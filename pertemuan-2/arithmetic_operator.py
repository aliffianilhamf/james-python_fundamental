""" 
Arithmetic operator adalah operasi layakanya di matematika, ada tambah, kurang kali bagi, dsb.
"""
# 1. Penjumlahan
print("Penjumlahan")
number_1 = 10 
number_2 = 30 
total = number_1 + number_2
print(f"Hasil dari {number_1} di tambah {number_2} adalah : {total}")

number_1 += number_2
print(f"Hasil dari {10} di tambah {number_2} adalah : {total}")

print()
# 2. Pengurangan
print("Pengurangan")
number_1 = 100 
number_2 = 30 
total = number_1 - number_2
print(f"Hasil dari {number_1} di kurang {number_2} adalah : {total}")

number_1 -= number_2
print(f"Hasil dari {100} di kurang {number_2} adalah : {total}")

print()
# 3. Perkalian
print("Perkalian")
number_1 = 100 
number_2 = 30 
total = number_1 * number_2
print(f"Hasil dari {number_1} di kali {number_2} adalah : {total}")

number_1 *= number_2
print(f"Hasil dari {100} di kali {number_2} adalah : {total}")

print()
# 4. Pembagian
print("Pembagian")
number_1 = 100 
number_2 = 30 
total = number_1 / number_2
print(f"Hasil dari {number_1} di bagi {number_2} adalah : {total}")

number_1 /= number_2
print(f"Hasil dari {100} di bagi {number_2} adalah : {total}")

print()
# 5. Pembagian Integer
print("Pembagian Integer")
number_1 = 100 
number_2 = 30 
total = number_1 // number_2
print(f"Hasil dari {number_1} di bagi {number_2} adalah : {total}")

number_1 //= number_2
print(f"Hasil dari {100} di bagi {number_2} adalah : {total}")

print()
# 6. Modulo (Sisa Hasil Bagi)
print("Modulo (Sisa Hasil Bagi)")
number_1 = 100 
number_2 = 30 
total = number_1 % number_2
print(f"Hasil dari {number_1} di bagi {number_2} adalah : {total}")

number_1 %= number_2
print(f"Hasil dari {100} di bagi {number_2} adalah : {total}")